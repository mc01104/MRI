///
///  \return   NONE
// --------------------------------------------------------------------------------------------
void MiniFlash::applyRotMatrix(int32_t axis)
{
	sROT_MATRIX mat;

	switch (axis){
		case X_Y:
			mat.dMat[0][0] = 1; 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 0;

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 1;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = 0;
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = 1;
			break;
		case X_Y_PRIME:
			mat.dMat[0][0] = 1; 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 0;

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = -1;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = 0;
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = -1;
			break;
		case Y_Z:
			mat.dMat[0][0] = 0;//cos(M_PI/2.0); 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 1;//sin(M_PI/2.0);

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 1;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = -1;//-sin(M_PI/2.0);
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = 0;//cos(M_PI/2.0);
			break;
		case Y_Z_PRIME:
			mat.dMat[0][0] = 0;//cos(-M_PI/2.0); 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = -1;//sin(-M_PI/2.0);

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 1;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = 1;//-sin(-M_PI/2.0);
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = 0;//cos(-M_PI/2.0);
			break;
		case X_Z_PRIME:
			mat.dMat[0][0] = 1; 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 0; 

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 0;//cos(M_PI/2.0);
			mat.dMat[1][2] = 1;//-sin(-M_PI/2.0);//-sin(M_PI/2.0);

			mat.dMat[2][0] = 0;
			mat.dMat[2][1] = -1;//sin(M_PI/2.0);
			mat.dMat[2][2] = 0;//cos(M_PI/2.0);
			break;
		case X_Z:
			mat.dMat[0][0] = 1; 
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 0; 

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 0;
			mat.dMat[1][2] = 1;

			mat.dMat[2][0] = 0;
			mat.dMat[2][1] = 1;
			mat.dMat[2][2] = 0;
			break;
		case ARB1:
			mat.dMat[0][0] = 0.707;
			mat.dMat[0][1] = 0;
			mat.dMat[0][2] = 0.707;

			mat.dMat[1][0] = 0;
			mat.dMat[1][1] = 1;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = -0.707;
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = 0.707;

			break;
		case ARB2:
			mat.dMat[0][0] = 0.57735;
			mat.dMat[0][1] = -0.57735;
			mat.dMat[0][2] = -0.57735;

			mat.dMat[1][0] = 0.57735;
			mat.dMat[1][1] = 0.788675;
			mat.dMat[1][2] = -0.211325;

			mat.dMat[2][0] = 0.57735;
			mat.dMat[2][1] = -0.211325;
			mat.dMat[2][2] = 0.788675;

			break;
		case X_X:
		case Y_Y:
		case Z_Z:
			mat.dMat[0][0] = 0; 
			mat.dMat[0][1] = 1;
			mat.dMat[0][2] = 0;

			mat.dMat[1][0] = 1;
			mat.dMat[1][1] = 0;
			mat.dMat[1][2] = 0;

			mat.dMat[2][0] = 0;
			mat.dMat[2][1] = 0;
			mat.dMat[2][2] = 1;
			break;
		default:
			break;
	}
	m_asSLC[0].setROT_MATRIX(mat);
}
